#!/usr/bin/env python3

def format_p3(p3: int) -> str:
    low = p3 & 0xFFFFFFFFFFFFFFFF
    high = p3 >> 64
    if high == 0:
        return f"{low}ULL"
    return f"((tc48_u128b){high}ULL << 64 | {low}ULL)"

def main() -> int:
    print("// NOTE: this file is auto-generated by scripts/gen/comptime-pow3.py")
    print("//       do not modify this file directly")
    print("#pragma once")
    print()
    print("#include <tc48/defs.h>")
    print()

    max_trits = 81
    for i in range(max_trits):
        p3 = 3**i
        print(f"#define _TC48_COMPTIME_POW3_{i} {format_p3(p3)}")

    print()
    print("#define TC48_COMPTIME_POW3(N) _TC48_COMPTIME_POW3_##N")
    return 0

if __name__ == "__main__":
    exit(main())
