#!/usr/bin/env python3

indent = "    "

def gen_macro(name: str, bits: int, typename: str) -> list[str]:
    args: list[str] = [f"T{i}" for i in range(bits-1, -1, -1)]
    args_str: str = ", ".join(args)

    parts = [f"_TC48_T(T{i}, {i})" for i in range(bits - 1, -1, -1)]
    parts_per_line = 4
    lines = []
    for i in range(0, len(parts), parts_per_line):
        chunk = parts[i:i+parts_per_line]
        lines.append(" + ".join(chunk))

    macro_start = f"#define TC48_{name.upper()}({args_str}) \\\n"
    cast = f"{indent}(({typename})( \\\n"
    
    res = []
    if len(lines) == 1:
        res.append(f"{macro_start}{cast}{lines[0]}))")
    else:
        res.append(f"{macro_start}{cast}{lines[0]} + \\")
        for i in range(1, len(lines)):
            l = indent*2 + lines[i]
            if i < len(lines) - 1:
                l += " + \\"
            else:
                l += "))"
            res.append(l)
    
    return res

def main() -> int:
    print("// NOTE: this file is auto-generated by scripts/gen/word-lits.py")
    print("//       do not modify this file directly")
    print("#pragma once")
    print()

    types: tuple = (
        ("doublet",    2,  "tc48_doublet"),
        ("triplet",    3,  "tc48_triplet"),
        ("quadruplet", 4,  "tc48_quadruplet"),
        ("tryte",      6,  "tc48_tryte"),
        ("quarter",    12, "tc48_quarter"),
        ("half",       24, "tc48_half"),
        ("word",       48, "tc48_word"),
    )

    for name, bits, typename in types:
        lines: list[str] = gen_macro(name, bits, typename)
        print('\n'.join(lines))
        print()
    return 0

if __name__ == "__main__":
    exit(main())
